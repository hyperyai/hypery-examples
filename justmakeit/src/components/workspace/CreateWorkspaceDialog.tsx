/**
 * Create Workspace Dialog
 * Modal for creating new workspaces with templates
 */

'use client';

import { useState } from 'react';
import type { ReactNode } from 'react';
import { WORKSPACE_TEMPLATES, getTemplateNames } from '@/lib/templates/workspace-templates';

interface CreateWorkspaceDialogProps {
  isOpen: boolean;
  onClose: () => void;
  onCreate: (name: string, description?: string, templateId?: string) => void;
}

export function CreateWorkspaceDialog({
  isOpen,
  onClose,
  onCreate,
}: CreateWorkspaceDialogProps) {
  const [name, setName] = useState('');
  const [description, setDescription] = useState('');
  const [selectedTemplate, setSelectedTemplate] = useState<string>('blank');

  if (!isOpen) return null;

  const handleSubmit = (e: React.FormEvent) => {
    e.preventDefault();
    if (!name.trim()) return;

    onCreate(name, description || undefined, selectedTemplate);
    
    // Reset form
    setName('');
    setDescription('');
    setSelectedTemplate('blank');
    onClose();
  };

  return (
    <>
      {/* Backdrop */}
      <div
        className="fixed inset-0 bg-black/50 z-40"
        onClick={onClose}
      />

      {/* Dialog */}
      <div className="fixed inset-0 flex items-center justify-center z-50 p-4">
        <div className="bg-[var(--bg-secondary)] border border-[var(--border-primary)] rounded-lg shadow-xl max-w-2xl w-full max-h-[90vh] overflow-y-auto">
          {/* Header */}
          <div className="flex items-center justify-between p-6 border-b border-[var(--border-primary)]">
            <h2 className="text-xl font-semibold text-[var(--text-secondary)]">Create New Workspace</h2>
            <button
              onClick={onClose}
              className="p-1 hover:bg-[var(--border-primary)] rounded text-[var(--text-tertiary)] hover:text-[var(--text-secondary)]"
            >
              <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M6 18L18 6M6 6l12 12" />
              </svg>
            </button>
          </div>

          {/* Form */}
          <form onSubmit={handleSubmit} className="p-6 space-y-6">
            {/* Workspace Name */}
            <div>
              <label htmlFor="workspace-name" className="block text-sm font-medium text-[var(--text-secondary)] mb-2">
                Workspace Name *
              </label>
              <input
                id="workspace-name"
                type="text"
                value={name}
                onChange={(e) => setName(e.target.value)}
                placeholder="My Awesome Project"
                className="w-full px-3 py-2 bg-[#3C3C3C] border border-[var(--border-primary)] rounded text-[var(--text-secondary)] placeholder-gray-500 focus:outline-none focus:ring-2 focus:ring-[var(--border-focus)]"
                required
              />
            </div>

            {/* Description */}
            <div>
              <label htmlFor="workspace-desc" className="block text-sm font-medium text-[var(--text-secondary)] mb-2">
                Description (Optional)
              </label>
              <textarea
                id="workspace-desc"
                value={description}
                onChange={(e) => setDescription(e.target.value)}
                placeholder="A brief description of your project"
                rows={2}
                className="w-full px-3 py-2 bg-[#3C3C3C] border border-[var(--border-primary)] rounded text-[var(--text-secondary)] placeholder-gray-500 focus:outline-none focus:ring-2 focus:ring-[var(--border-focus)] resize-none"
              />
            </div>

            {/* Template Selection */}
            <div>
              <label className="block text-sm font-medium text-[var(--text-secondary)] mb-3">
                Choose a Template
              </label>
              <div className="grid grid-cols-1 md:grid-cols-2 gap-3">
                {getTemplateNames().map((templateId) => {
                  const template = WORKSPACE_TEMPLATES[templateId];
                  return (
                    <button
                      key={templateId}
                      type="button"
                      onClick={() => setSelectedTemplate(templateId)}
                      className={`p-4 rounded border-2 transition-all text-left ${
                        selectedTemplate === templateId
                          ? 'border-[var(--border-focus)] bg-[var(--accent-hover)]/10'
                          : 'border-[var(--border-primary)] hover:border-[#505050] bg-[var(--bg-tertiary)]'
                      }`}
                    >
                      <div className="flex items-start gap-3">
                        <div className={`mt-0.5 ${selectedTemplate === templateId ? 'text-blue-400' : 'text-[var(--text-tertiary)]'}`}>
                          {getTemplateIcon(templateId)}
                        </div>
                        <div className="flex-1">
                          <div className="font-medium text-[var(--text-secondary)]">{template.name}</div>
                          <div className="text-xs text-[var(--text-tertiary)] mt-1">{template.description}</div>
                          <div className="text-xs text-[var(--text-disabled)] mt-2">
                            {Object.keys(template.files).length} starter files
                          </div>
                        </div>
                      </div>
                    </button>
                  );
                })}
              </div>
            </div>

            {/* Actions */}
            <div className="flex items-center justify-end gap-3 pt-4 border-t border-[var(--border-primary)]">
              <button
                type="button"
                onClick={onClose}
                className="px-4 py-2 text-sm text-[var(--text-secondary)] hover:text-gray-100 hover:bg-[var(--border-primary)] rounded ide-transition"
              >
                Cancel
              </button>
              <button
                type="submit"
                disabled={!name.trim()}
                className="px-4 py-2 text-sm bg-[var(--accent-primary)] hover:bg-[var(--accent-hover)] text-[var(--text-primary)] rounded ide-transition disabled:opacity-50 disabled:cursor-not-allowed"
              >
                Create Workspace
              </button>
            </div>
          </form>
        </div>
      </div>
    </>
  );
}

